package com.app.cryptocurrencyapp.di.module;


import com.app.cryptocurrencyapp.MainActivity;
import com.app.cryptocurrencyapp.recyclerAdapter.RecyclerViewAdapter;
import com.app.cryptocurrencyapp.di.scopes.ActivityScope;

import dagger.Module;
import dagger.Provides;

@Module(includes = {MainActivityContextModule.class})
public class AdapterModule {

    @Provides
    @ActivityScope
    public RecyclerViewAdapter getCoinList(RecyclerViewAdapter.ClickListener clickListener) {
        return new RecyclerViewAdapter(clickListener);
    }

    @Provides
    @ActivityScope
    public RecyclerViewAdapter.ClickListener getClickListener(MainActivity mainActivity) {
        return mainActivity;
    }
}
